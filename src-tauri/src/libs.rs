//! 外部ライブラリ（`walkdir` 等）に依存する処理を集約するモジュール。
//!
//! 本モジュールの内部では外部 crate の型を扱ってよいが、各サブモジュールが
//! 公開する API は `std` の型と独自定義のエラー型のみで構成し、外部 crate の
//! 型を境界の外（`crate::libs` の外）に漏らさないこと。これにより、将来的な
//! ライブラリ差し替え（例: `walkdir` → `jwalk` / 自作 `read_dir` 再帰）が
//! `libs` 配下の変更だけで完結する。
//!
//! # 配置基準
//!
//! 「重い外部依存」をここに集約することで、影響範囲の波及を 1 箇所に閉じ込める。
//!
//! - **集約する**: 重い I/O / 走査 / OS 依存 / ネットワーク等を伴う crate。
//!   例: `walkdir`（再帰走査）/ `notify`（ファイル監視）/ `reqwest`（HTTP）
//! - **集約しない**: Rust エコシステムで事実上標準の型変換・派生 crate。
//!   例: `serde` / `serde_json` / `serde_yaml_ng` / `thiserror` /
//!   `anyhow`（直接使ってよい。差し替え対象外として扱う）

