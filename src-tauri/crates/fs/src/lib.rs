//! `spec-board` のファイルシステム I/O（再帰スキャン、将来的にはファイル監視・
//! 読み書き等）を集約するサブクレート。
//!
//! 重い外部 crate（`walkdir`、将来的には `notify` 等）に依存する処理を 1 箇所に
//! 閉じ込めることで、ライブラリ差し替えの影響範囲を本サブクレート内に限定する。
//!
//! # 境界規約
//!
//! 本クレートが公開する API は `std` の型と独自定義のエラー型のみで構成し、
//! 外部 crate の型（例: `walkdir::DirEntry` / `walkdir::Error`）を境界の外に
//! 漏らさないこと。これにより、将来的なライブラリ差し替え（例: `walkdir`
//! → `jwalk` / 自作 `read_dir` 再帰）が本クレート内の変更だけで完結する。
//!
//! # 配置基準
//!
//! - **集約する**: 重い I/O / 走査 / OS 依存 / ネットワーク等を伴う crate
//!   - 例: `walkdir`（再帰走査）、`notify`（ファイル監視。Issue で予定）
//! - **集約しない**: Rust エコシステムで事実上標準の型変換・派生系
//!   - 例: `serde` / `serde_json` / `serde_yaml_ng` / `thiserror` / `anyhow`
//!
//! # 命名規則
//!
//! - パッケージ名: `spec-board-fs`（ハイフン）
//! - Rust 識別子: `spec_board_fs`（アンダースコア）

